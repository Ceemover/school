#include "libft.h"

int				ft_free_arr(char **arr)
{
	int	i;

	i = -1;
	if (!arr || !*arr)
		return (0);
	while (arr[++i])
		free1(&arr[i]);
	free(arr);
	arr = NULL;
	return (0);
}
