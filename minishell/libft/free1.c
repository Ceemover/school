#include "libft.h"

int				free1(char **str)
{
	int	len;
	int	i;

	i = -1;
	if (*str)
	{
		len = ft_strlen(*str);
		while (++i < len)
			*(*str + i) = '@';
		free(*str);
		*str = NULL;
	}
	return (0);
}
